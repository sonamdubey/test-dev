function FilterDealers() {
    var city = $('#drpDealerCity').val();
    if (city == -1) {
        $('#dealerListsData li').show();
        $('#dealerCityName').text('INDIA');
    }
    else {
        $('#dealerListsData li').each(function () {
            $('#dealerCityName').text(city);
            ($(this).children('span:first').text() == city) ? $(this).show() : $(this).hide();            
        });
    }
}

